/// Integration test: complete pipeline from CheckReceipt to fingerprint lookup set.
/// This exercises: receipt → baseline_from_receipt → merge_false_positive_baselines → false_positive_fingerprint_set
#[test]
fn integration_full_pipeline_receipt_to_fingerprint_set() {
    // Step 1: Create two receipts with findings
    let findings1 = vec![make_finding("rust.no_unwrap", "src/lib.rs", 10, "unwrap()")];
    let findings2 = vec![make_finding("rust.no_debug", "src/main.rs", 20, "dbg!()")];

    let receipt1 = make_receipt(findings1);
    let receipt2 = make_receipt(findings2);

    // Step 2: Build baselines from receipts
    let baseline1 = baseline_from_receipt(&receipt1);
    let baseline2 = baseline_from_receipt(&receipt2);

    // Step 3: Merge baselines (base=existing baseline, incoming=new check)
    let merged = merge_false_positive_baselines(&baseline1, &baseline2);

    // Step 4: Extract fingerprint set for fast lookup
    let fp_set = false_positive_fingerprint_set(&merged);

    // Verify the full pipeline worked
    assert_eq!(fp_set.len(), 2);
    assert!(fp_set.contains(&baseline1.entries[0].fingerprint));
    assert!(fp_set.contains(&baseline2.entries[0].fingerprint));
}

/// Integration test: full pipeline with an existing baseline (simulating --write-false-positive-baseline flow).
/// This simulates how diffguard main binary uses these functions:
/// - Generate baseline from current check receipt
/// - Load existing baseline from disk (or use empty)
/// - Merge existing + generated
/// - Extract fingerprint set for filtering
#[test]
fn integration_existing_plus_generated_baseline_pipeline() {
    // Simulate: existing baseline from previous runs (with a manually curated note)
    let mut existing = FalsePositiveBaseline::default();
    existing.entries.push(FalsePositiveEntry {
        fingerprint: "existing_fp".to_string(),
        rule_id: "old.rule".to_string(),
        path: "old.rs".to_string(),
        line: 5,
        note: Some("intentionally suppressed".to_string()),
    });

    // Current check run generates new findings
    let new_findings = vec![
        make_finding("rust.no_unwrap", "src/lib.rs", 10, "unwrap()"),
        make_finding("rust.no_debug", "src/main.rs", 20, "dbg!()"),
    ];
    let receipt = make_receipt(new_findings);
    let generated = baseline_from_receipt(&receipt);

    // Merge: existing (base) + generated (incoming)
    let merged = merge_false_positive_baselines(&existing, &generated);

    // Extract fingerprint set
    let fp_set = false_positive_fingerprint_set(&merged);

    // Should have 3 total fingerprints
    assert_eq!(fp_set.len(), 3);
    assert!(fp_set.contains("existing_fp"));

    // Verify the existing note was preserved (merge strategy: base fills empty fields)
    let existing_entry = merged
        .entries
        .iter()
        .find(|e| e.fingerprint == "existing_fp")
        .expect("existing_fp should exist");
    assert_eq!(
        existing_entry.note.as_deref(),
        Some("intentionally suppressed")
    );
}

/// Integration test: full pipeline is deterministic (same inputs → same outputs).
#[test]
fn integration_full_pipeline_is_deterministic() {
    let findings = vec![make_finding("rust.no_unwrap", "src/lib.rs", 42, "unwrap()")];
    let receipt = make_receipt(findings);

    let baseline = baseline_from_receipt(&receipt);
    let merged1 = merge_false_positive_baselines(&baseline, &FalsePositiveBaseline::default());
    let merged2 = merge_false_positive_baselines(&baseline, &FalsePositiveBaseline::default());

    let set1 = false_positive_fingerprint_set(&merged1);
    let set2 = false_positive_fingerprint_set(&merged2);

    assert_eq!(set1, set2);
}

