//! Integration tests for diffguard-analytics component handoffs.
//!
//! These tests exercise the seams between analytics functions:
//! 1. `fingerprint_for_finding` → `baseline_from_receipt` (fingerprint feeds into baseline)
//! 2. `baseline_from_receipt` → `merge_false_positive_baselines` (baseline feeds into merge)
//! 3. `merge_false_positive_baselines` → `false_positive_fingerprint_set` (merged baseline feeds into lookup set)
//! 4. Full pipeline: receipt → fingerprint → baseline → merge → fingerprint_set
//!
//! The `#[must_use]` attribute on `merge_false_positive_baselines` means callers MUST capture
//! the return value. These integration tests verify that capturing the return value produces
//! correct end-to-end behavior.

use diffguard_analytics::{
    FALSE_POSITIVE_BASELINE_SCHEMA_V1, FalsePositiveBaseline, FalsePositiveEntry, TrendHistory,
    append_trend_run, baseline_from_receipt, false_positive_fingerprint_set,
    fingerprint_for_finding, merge_false_positive_baselines, normalize_false_positive_baseline,
    summarize_trend_history, trend_run_from_receipt,
};
use diffguard_types::{
    CheckReceipt, DiffMeta, Finding, Scope, Severity, ToolMeta, Verdict, VerdictCounts,
    VerdictStatus,
};

// ============================================================================
// Helper Factories
// ============================================================================

fn make_finding(rule_id: &str, path: &str, line: u32, match_text: &str) -> Finding {
    Finding {
        rule_id: rule_id.to_string(),
        severity: Severity::Error,
        message: "Test message".to_string(),
        path: path.to_string(),
        line,
        column: Some(1),
        match_text: match_text.to_string(),
        snippet: format!("line {} {}", line, match_text),
    }
}

fn make_receipt(findings: Vec<Finding>) -> CheckReceipt {
    CheckReceipt {
        schema: "diffguard.check.v1".to_string(),
        tool: ToolMeta {
            name: "diffguard".to_string(),
            version: "0.2.0".to_string(),
        },
        diff: DiffMeta {
            base: "origin/main".to_string(),
            head: "HEAD".to_string(),
            context_lines: 3,
            scope: Scope::Added,
            files_scanned: 1,
            lines_scanned: 10,
        },
        findings,
        verdict: Verdict {
            status: VerdictStatus::Fail,
            counts: VerdictCounts {
                info: 0,
                warn: 0,
                error: 1,
                suppressed: 0,
            },
            reasons: vec![],
        },
        timing: None,
    }
}

// ============================================================================
// Hand-off 1: fingerprint_for_finding → baseline_from_receipt
// ============================================================================

/// Integration test: fingerprint_from_finding is used correctly by baseline_from_receipt.
/// The fingerprint generated by `fingerprint_for_finding` must appear as the fingerprint
/// field in entries created by `baseline_from_receipt`.
#[test]
fn integration_fingerprint_feeds_into_baseline_from_receipt() {
    let finding = make_finding("rust.no_unwrap", "src/lib.rs", 42, "unwrap()");
    let expected_fp = fingerprint_for_finding(&finding);

    let receipt = make_receipt(vec![finding]);
    let baseline = baseline_from_receipt(&receipt);

    assert_eq!(baseline.entries.len(), 1);
    assert_eq!(baseline.entries[0].fingerprint, expected_fp);
    assert_eq!(baseline.entries[0].rule_id, "rust.no_unwrap");
    assert_eq!(baseline.entries[0].path, "src/lib.rs");
    assert_eq!(baseline.entries[0].line, 42);
}

/// Integration test: multiple findings each produce unique fingerprints in baseline.
#[test]
fn integration_multiple_fingerprints_in_baseline() {
    let findings = vec![
        make_finding("rust.no_unwrap", "src/lib.rs", 10, "unwrap()"),
        make_finding("rust.no_debug", "src/main.rs", 20, "dbg!()"),
        make_finding("rust.no_expect", "src/lib.rs", 30, "expect()"),
    ];
    let receipt = make_receipt(findings);
    let baseline = baseline_from_receipt(&receipt);

    let fingerprints: Vec<_> = baseline.entries.iter().map(|e| &e.fingerprint).collect();
    assert_eq!(fingerprints.len(), 3);
    // All fingerprints should be unique
    let mut sorted = fingerprints.clone();
    sorted.sort();
    sorted.dedup();
    assert_eq!(sorted.len(), 3, "All fingerprints should be unique");
}

