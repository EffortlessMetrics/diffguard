// Hand-off 2: baseline_from_receipt → merge_false_positive_baselines
// ============================================================================

/// Integration test: baseline_from_receipt output is a valid input to merge_false_positive_baselines.
/// This tests the pipeline: receipt → baseline → merge.
#[test]
fn integration_baseline_from_receipt_feeds_into_merge() {
    let finding = make_finding("rust.no_unwrap", "src/lib.rs", 42, "unwrap()");
    let receipt = make_receipt(vec![finding]);

    let generated = baseline_from_receipt(&receipt);

    // Empty base + generated = normalized generated
    let empty = FalsePositiveBaseline::default();
    let merged = merge_false_positive_baselines(&empty, &generated);

    assert_eq!(merged.entries.len(), 1);
    assert_eq!(merged.schema, FALSE_POSITIVE_BASELINE_SCHEMA_V1);
    assert_eq!(merged.entries[0].rule_id, "rust.no_unwrap");
}

/// Integration test: two baselines from receipts merge correctly.
#[test]
fn integration_two_receipt_baselines_merge_correctly() {
    let findings1 = vec![make_finding("rust.no_unwrap", "src/lib.rs", 10, "unwrap()")];
    let findings2 = vec![make_finding("rust.no_debug", "src/main.rs", 20, "dbg!()")];

    let receipt1 = make_receipt(findings1);
    let receipt2 = make_receipt(findings2);

    let baseline1 = baseline_from_receipt(&receipt1);
    let baseline2 = baseline_from_receipt(&receipt2);

    // Merge: base=baseline1, incoming=baseline2
    let merged = merge_false_positive_baselines(&baseline1, &baseline2);

    assert_eq!(merged.entries.len(), 2);
    let rules: Vec<_> = merged.entries.iter().map(|e| e.rule_id.as_str()).collect();
    assert!(rules.contains(&"rust.no_unwrap"));
    assert!(rules.contains(&"rust.no_debug"));
}

