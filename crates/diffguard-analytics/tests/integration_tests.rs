/// Integration test: merge_false_positive_baselines output is a valid input to false_positive_fingerprint_set.
/// This tests the pipeline: merge → fingerprint_set.
#[test]
fn integration_merged_baseline_feeds_into_fingerprint_set() {
    let base = FalsePositiveBaseline {
        schema: FALSE_POSITIVE_BASELINE_SCHEMA_V1.to_string(),
        entries: vec![FalsePositiveEntry {
            fingerprint: "fp_base".to_string(),
            rule_id: "rule.base".to_string(),
            path: "base.rs".to_string(),
            line: 1,
            note: Some("base note".to_string()),
        }],
    };
    let incoming = FalsePositiveBaseline {
        schema: FALSE_POSITIVE_BASELINE_SCHEMA_V1.to_string(),
        entries: vec![FalsePositiveEntry {
            fingerprint: "fp_incoming".to_string(),
            rule_id: "rule.incoming".to_string(),
            path: "incoming.rs".to_string(),
            line: 2,
            note: None,
        }],
    };

    let merged = merge_false_positive_baselines(&base, &incoming);
    let fp_set = false_positive_fingerprint_set(&merged);

    assert_eq!(fp_set.len(), 2);
    assert!(fp_set.contains("fp_base"));
    assert!(fp_set.contains("fp_incoming"));
}

