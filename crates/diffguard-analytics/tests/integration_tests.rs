/// Integration test: append_trend_run + summarize_trend_history pipeline.
#[test]
fn integration_trend_append_and_summarize() {
    let findings = vec![make_finding("rust.no_unwrap", "src/lib.rs", 42, "unwrap()")];
    let receipt = make_receipt(findings);

    let run1 = trend_run_from_receipt(
        &receipt,
        "2026-01-01T00:00:00Z",
        "2026-01-01T00:00:01Z",
        1000,
    );
    let mut run2 = trend_run_from_receipt(
        &receipt,
        "2026-01-02T00:00:00Z",
        "2026-01-02T00:00:01Z",
        1000,
    );
    run2.findings = 5;
    run2.counts.error = 5;

    let history = TrendHistory::default();
    let history = append_trend_run(history, run1, None);
    let history = append_trend_run(history, run2, None);

    let summary = summarize_trend_history(&history);

    assert_eq!(summary.run_count, 2);
    assert_eq!(summary.total_findings, 6); // 1 + 5
    let delta = summary.delta_from_previous.expect("should have delta");
    assert_eq!(delta.findings, 4); // 5 - 1
}

/// Integration test: trend pipeline with max_runs trimming.
#[test]
fn integration_trend_with_max_runs_trimming() {
    let findings = vec![make_finding("rust.no_unwrap", "src/lib.rs", 42, "unwrap()")];
    let receipt = make_receipt(findings);

    let history = TrendHistory::default();
    let history = append_trend_run(
        history,
        trend_run_from_receipt(
            &receipt,
            "2026-01-01T00:00:00Z",
            "2026-01-01T00:00:01Z",
            1000,
        ),
        Some(3),
    );
    let history = append_trend_run(
        history,
        trend_run_from_receipt(
            &receipt,
            "2026-01-02T00:00:00Z",
            "2026-01-02T00:00:01Z",
            1000,
        ),
        Some(3),
    );
    let history = append_trend_run(
        history,
        trend_run_from_receipt(
            &receipt,
            "2026-01-03T00:00:00Z",
            "2026-01-03T00:00:01Z",
            1000,
        ),
        Some(3),
    );
    let history = append_trend_run(
        history,
        trend_run_from_receipt(
            &receipt,
            "2026-01-04T00:00:00Z",
            "2026-01-04T00:00:01Z",
            1000,
        ),
        Some(3),
    );

    // Should be trimmed to 3 (oldest dropped)
    assert_eq!(history.runs.len(), 3);
    // Oldest should be from 2026-01-02 (2026-01-01 dropped)
    assert_eq!(history.runs[0].started_at, "2026-01-02T00:00:00Z");
}

