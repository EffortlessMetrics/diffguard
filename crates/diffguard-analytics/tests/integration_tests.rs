/// Integration test: normalize_false_positive_baseline cleans up before merge.
#[test]
fn integration_normalize_before_merge() {
    // Create a baseline with unsorted entries and duplicates (simulating corrupted/manual baseline)
    let dirty = FalsePositiveBaseline {
        schema: String::new(), // Missing schema
        entries: vec![
            FalsePositiveEntry {
                fingerprint: "zzz".to_string(),
                rule_id: "z.rule".to_string(),
                path: "z.rs".to_string(),
                line: 3,
                note: None,
            },
            FalsePositiveEntry {
                fingerprint: "aaa".to_string(),
                rule_id: "a.rule".to_string(),
                path: "a.rs".to_string(),
                line: 1,
                note: None,
            },
            // Duplicate fingerprint
            FalsePositiveEntry {
                fingerprint: "aaa".to_string(),
                rule_id: "a.rule".to_string(),
                path: "a.rs".to_string(),
                line: 1,
                note: None,
            },
        ],
    };

    // Normalize first, then merge
    let normalized = normalize_false_positive_baseline(dirty);
    let merged = merge_false_positive_baselines(&normalized, &FalsePositiveBaseline::default());

    // Should be sorted, deduplicated, and have correct schema
    assert_eq!(merged.schema, FALSE_POSITIVE_BASELINE_SCHEMA_V1);
    assert_eq!(merged.entries.len(), 2); // zzz and aaa (aaa was deduplicated)
    assert_eq!(merged.entries[0].fingerprint, "aaa"); // Sorted first
    assert_eq!(merged.entries[1].fingerprint, "zzz"); // Sorted second
}
