//! Property-based tests for diffguard-testkit invariants.
//!
//! These tests verify important invariants that should hold across all generated
//! inputs, not just specific examples. Property tests run hundreds of iterations
//! to find edge cases that example-based tests might miss.
//!
//! Invariants tested:
//! - Roundtrip: serialize → deserialize preserves identity
//! - Bounded: generated values stay within MAX_* limits
//! - Valid: generated patterns are syntactically valid regex/globs
//! - MatchMode: all generated RuleConfigs have match_mode = MatchMode::Any

use diffguard_testkit::arb::{self, MAX_PATHS_PER_RULE, MAX_PATTERNS_PER_RULE};
use diffguard_types::{MatchMode, RuleConfig, Severity};
use proptest::prelude::*;
use proptest::strategy::ValueTree;
use proptest::test_runner::TestRunner;

/// Property: ConfigFile roundtrips through JSON serialization.
///
/// For all generated ConfigFiles, serialize to JSON and deserialize back
/// should produce an identical ConfigFile.
#[test]
fn config_file_roundtrip() {
    let mut runner = TestRunner::default();
    let strategy = arb::arb_config_file();

    for _ in 0..50 {
        let config = strategy.new_tree(&mut runner).unwrap().current();
        let json = serde_json::to_string(&config).unwrap();
        let parsed: diffguard_types::ConfigFile = serde_json::from_str(&json).unwrap();
        assert_eq!(
            config, parsed,
            "ConfigFile should roundtrip through JSON serialization"
        );
    }
}

