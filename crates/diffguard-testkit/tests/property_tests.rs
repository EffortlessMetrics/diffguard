/// Property: MatchMode roundtrips through JSON serialization.
///
/// MatchMode::Any and MatchMode::Absent should roundtrip correctly.
#[test]
fn match_mode_roundtrip() {
    // Test MatchMode::Any
    let json_any = serde_json::to_string(&MatchMode::Any).unwrap();
    let parsed_any: MatchMode = serde_json::from_str(&json_any).unwrap();
    assert_eq!(
        MatchMode::Any,
        parsed_any,
        "MatchMode::Any should roundtrip"
    );

    // Test MatchMode::Absent
    let json_absent = serde_json::to_string(&MatchMode::Absent).unwrap();
    let parsed_absent: MatchMode = serde_json::from_str(&json_absent).unwrap();
    assert_eq!(
        MatchMode::Absent,
        parsed_absent,
        "MatchMode::Absent should roundtrip"
    );

    // Test MatchMode::default() (which is Any)
    let default = MatchMode::default();
    let json_default = serde_json::to_string(&default).unwrap();
    let parsed_default: MatchMode = serde_json::from_str(&json_default).unwrap();
    assert_eq!(
        default, parsed_default,
        "MatchMode::default() should roundtrip"
    );
}

